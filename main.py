def _get_api_key() -> str:
    from neamt.config import get_config  # neamt is not in the blocked list
    key = get_config("anthropic_api_key")
    if not key:
        raise RuntimeError(
            "Anthropic API key not configured. "
            "Run: neamt config set anthropic_api_key <your-key>"
        )
    return key


def _slugify(text: str, max_len: int = 60) -> str:
    slug = re.sub(r"[^\w\s-]", "", text.lower())
    slug = re.sub(r"[\s_-]+", "-", slug).strip("-")
    return slug[:max_len]


def _format_duration(seconds: int) -> str:
    if not seconds:
        return "unknown"
    h, rem = divmod(seconds, 3600)
    m, s = divmod(rem, 60)
    return f"{h}h {m}m" if h else f"{m}m {s}s"


def _truncate_transcript(transcript: str) -> str:
    if len(transcript) <= _MAX_TRANSCRIPT_CHARS:
        return transcript
    half = _MAX_TRANSCRIPT_CHARS // 2
    return (
        transcript[:half]
        + "\n\n[... middle section omitted for length ...]\n\n"
        + transcript[-half:]
    )


